# Viết chương trình nhập vào số tiền gửi ban đầu, lãi suất ngân hàng theo tháng (%), và số tháng gửi. Sau đó tính và in ra tổng số tiền sau khi hết thời gian gửi theo công thức lãi kép:
# Số tiền sau = Số tiền ban đầu * ( 1 + lãi suất ) * số tháng

# Ví dụ:

# Nhập số tiền gửi ban đầu: 10,000,000 VND
# Nhập lãi suất hàng tháng: 0.5%
# Nhập số tháng gửi: 12
# Kết quả: Tổng số tiền sau 12 tháng.